import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { variant?: 'default' | 'glass' | 'glow' | 'gradient' }
>(({ className, variant = 'default', ...props }, ref) => {
  const variants = {
    default: "rounded-xl border bg-card text-card-foreground shadow-lg shadow-black/5",
    glass: "rounded-xl bg-card/60 backdrop-blur-xl text-card-foreground border border-white/10 shadow-xl",
    glow: "rounded-xl border border-primary/30 bg-card text-card-foreground shadow-lg shadow-primary/10 hover:shadow-primary/20 hover:border-primary/50 transition-all duration-300",
    gradient: "rounded-xl bg-gradient-to-br from-card via-card to-secondary/50 text-card-foreground border border-border/50 shadow-xl",
  }
  
  return (
    <div
      ref={ref}
      className={cn(variants[variant], className)}
      {...props}
    />
  )
})
Card.displayName = "Card"

